void GPIO_TC_003(void)
{
	GPIO_InitPC13();
	GPIO_SetPinPC13();
	GPIO_InitPA0();
	while(1)
	{
		if(GPIO_ReadPinPA0() == 0)
		{
			GPIO_ResetPinPC13();
		}else{
			GPIO_SetPinPC13();
		}
	}
}


